class CartModel {
  final String id;
  final String idProduct;
  final String image;
  final String name;
  final String description;
  final String size;
  final int quantity;

  CartModel({
    required this.id,
    required this.idProduct,
    required this.image,
    required this.name,
    required this.description,
    required this.size,
    required this.quantity,
  });
}
