class CoffeeModel {
  final String id;
  final String image;
  final String name;
  final String description;
  final double price;
  final int quantity;
  final double rate;
  final String type;
  final int buy;
  final String info;
  final List<String> size;

  CoffeeModel({
    required this.id,
    required this.image,
    required this.name,
    required this.description,
    required this.price,
    required this.quantity,
    required this.rate,
    required this.type,
    required this.buy,
    required this.info,
    required this.size,
  });
}
