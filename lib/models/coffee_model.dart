class CoffeeModel {
  final String id;
  final String image;
  final String name;
  final String description;
  final double price;
  final double rate;
  final String type;

  CoffeeModel({
    required this.id,
    required this.image,
    required this.name,
    required this.description,
    required this.price,
    required this.rate,
    required this.type,
  });
}
